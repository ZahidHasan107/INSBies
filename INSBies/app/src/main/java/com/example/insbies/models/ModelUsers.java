package com.example.insbies.models;

public class ModelUsers {

    String name,email,mobile,img,major,blood,address,position;

    public ModelUsers() {
    }

    public ModelUsers(String name, String email, String mobile, String img, String major, String blood, String address, String position) {
        this.name = name;
        this.email = email;
        this.mobile = mobile;
        this.img = img;
        this.major = major;
        this.blood = blood;
        this.address = address;
        this.position = position;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public String getMajor() {
        return major;
    }

    public void setMajor(String major) {
        this.major = major;
    }

    public String getBlood() {
        return blood;
    }

    public void setBlood(String blood) {
        this.blood = blood;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }
}
